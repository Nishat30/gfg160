class Solution {
  public:
    int getSingle(vector<int> &arr) {
        // code here
        unordered_map<int,int> mp;
        for(int i:arr){
            mp[i]++;
        }
        for(int i:arr){
            if(mp[i]==1) return i;
        }
    }
};


